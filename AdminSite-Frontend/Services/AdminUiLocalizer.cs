        public static string T(string key, string lang = "en")
        {
            var value = Normalize(lang) switch
            {
                "vi" => VietnameseUIText.T(key),
                "cn" => ChineseUIText.T(key),
                _ => null
            };

            if (!string.IsNullOrWhiteSpace(value))
            {
                return value;
            }

            return English.TryGetValue(key, out var en) ? en : key;
        }

        private static string Normalize(string? lang)
        {
            if (string.Equals(lang, "vi", StringComparison.OrdinalIgnoreCase)) return "vi";
            if (string.Equals(lang, "cn", StringComparison.OrdinalIgnoreCase)) return "cn";
            return "en";
        }
